/**
 * Generates CSS overrides so existing white-based Tailwind utilities
 * read correctly in light mode (prefers-color-scheme: light).
 */
import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const outPath = join(__dirname, "../src/styles/light-mode-overrides.css");

const INK = "17 24 39";

function esc(className) {
  return className.replace(/\//g, "\\/").replace(/\./g, "\\.").replace(/\[/g, "\\[").replace(/\]/g, "\\]");
}

function textOpacity(n) {
  const pct = n <= 1 ? n : n / 100;
  return `.${esc(`text-white/${n}`)} { color: rgb(${INK} / ${pct}); }`;
}

function textArbitrary(opacity) {
  return `.${esc(`text-white/[${opacity}]`)} { color: rgb(${INK} / ${opacity}); }`;
}

function bgOpacity(n) {
  const pct = n <= 1 ? n : n / 100;
  return `.${esc(`bg-white/${n}`)} { background-color: rgb(${INK} / ${pct}); }`;
}

function bgArbitrary(opacity) {
  return `.${esc(`bg-white/[${opacity}]`)} { background-color: rgb(${INK} / ${opacity}); }`;
}

function borderOpacity(n) {
  const pct = n <= 1 ? n : n / 100;
  return `.${esc(`border-white/${n}`)} { border-color: rgb(${INK} / ${pct}); }`;
}

function borderArbitrary(opacity) {
  return `.${esc(`border-white/[${opacity}]`)} { border-color: rgb(${INK} / ${opacity}); }`;
}

function ringArbitrary(opacity) {
  return `.${esc(`ring-white/[${opacity}]`)} { --tw-ring-color: rgb(${INK} / ${opacity}); }`;
}

function fromArbitrary(opacity) {
  return `.${esc(`from-white/[${opacity}]`)} { --tw-gradient-from: rgb(${INK} / ${opacity}) var(--tw-gradient-from-position); --tw-gradient-to: rgb(${INK} / 0) var(--tw-gradient-to-position); --tw-gradient-stops: var(--tw-gradient-from), var(--tw-gradient-to); }`;
}

function viaArbitrary(opacity) {
  return `.${esc(`via-white/[${opacity}]`)} { --tw-gradient-to: rgb(${INK} / 0) var(--tw-gradient-to-position); --tw-gradient-stops: var(--tw-gradient-from), rgb(${INK} / ${opacity}) var(--tw-gradient-via-position), var(--tw-gradient-to); }`;
}

function hover(rule) {
  const inner = rule.replace(/^\./, "");
  return `.hover\\:${inner.replace(/\\/g, "\\\\")}:hover { ${rule.match(/\{([^}]+)\}/)[1]} }`;
}

const textFracs = [15, 16, 20, 22, 25, 28, 40, 50, 55, 60, 70, 80, 85, 90];
const textArb = ["0.02", "0.03", "0.04", "0.16"];
const bgFracs = [10, 15, 20];
const bgArb = ["0.02", "0.025", "0.03", "0.04", "0.05", "0.06", "0.07", "0.08"];
const borderFracs = [10, 20];
const borderArb = ["0.04", "0.06", "0.07", "0.08"];
const ringArb = ["0.06", "0.08"];
const fromArb = ["0.02", "0.08"];
const viaArb = ["0.08"];

const lines = [
  "/* Auto-generated by scripts/generate-light-mode-overrides.mjs — do not edit by hand */",
  "@media (prefers-color-scheme: light) {",
  `  .text-white { color: rgb(${INK}); }`,
  ...textFracs.map((n) => "  " + textOpacity(n)),
  ...textArb.map((n) => "  " + textArbitrary(n)),
  ...bgFracs.map((n) => "  " + bgOpacity(n)),
  ...bgArb.map((n) => "  " + bgArbitrary(n)),
  ...borderFracs.map((n) => "  " + borderOpacity(n)),
  ...borderArb.map((n) => "  " + borderArbitrary(n)),
  ...ringArb.map((n) => "  " + ringArbitrary(n)),
  ...fromArb.map((n) => "  " + fromArbitrary(n)),
  ...viaArb.map((n) => "  " + viaArbitrary(n)),
  "",
  "  /* Hover / group-hover remaps */",
  `  .hover\\:text-white:hover { color: rgb(${INK}); }`,
  `  .hover\\:text-white\\/60:hover { color: rgb(${INK} / 0.6); }`,
  `  .hover\\:border-white\\/10:hover { border-color: rgb(${INK} / 0.1); }`,
  `  .hover\\:border-white\\/20:hover { border-color: rgb(${INK} / 0.2); }`,
  `  .hover\\:bg-white\\/\\[0\\.08\\]:hover { background-color: rgb(${INK} / 0.08); }`,
  `  .hover\\:bg-white\\/\\[0\\.04\\]:hover { background-color: rgb(${INK} / 0.04); }`,
  `  .hover\\:bg-white\\/\\[0\\.03\\]:hover { background-color: rgb(${INK} / 0.03); }`,
  `  .hover\\:bg-white\\/\\[0\\.05\\]:hover { background-color: rgb(${INK} / 0.05); }`,
  `  .group:hover .group-hover\\:border-white\\/15 { border-color: rgb(${INK} / 0.15); }`,
  `  .group:hover .group-hover\\:border-white\\/\\[0\\.15\\] { border-color: rgb(${INK} / 0.15); }`,
  `  .group:hover .group-hover\\:bg-white\\/\\[0\\.04\\] { background-color: rgb(${INK} / 0.04); }`,
  `  .group:hover .group-hover\\:bg-white\\/10 { background-color: rgb(${INK} / 0.1); }`,
  "",
  "  /* Keep white text on gradient / primary buttons */",
  "  .bg-gradient.text-white,",
  "  .header-cta,",
  "  a.bg-gradient,",
  "  button.bg-gradient { color: #ffffff; }",
  "",
  "  /* Logo — light mark on dark bg becomes dark on light bg */",
  "  .site-header__logo { filter: brightness(0) saturate(100%); }",
  "  footer .site-header__logo, footer img[alt*=\"KINEXIS\"] { filter: brightness(0) saturate(100%); }",
  "",
  "  /* Lighter grain in light mode */",
  "  .grain-overlay { opacity: 0.015; mix-blend-mode: multiply; }",
  "}",
  "",
];

writeFileSync(outPath, lines.join("\n"));
console.log(`Wrote ${outPath}`);
